#include "pushswap.h"

int	ft_isdigit(char *s)
{
	int	i;

	if (!*s)
		return (0);
	i = 0;
	while (s[i])
	{
		if ((s[i] >= '0' && s[i] <= '9') || (s[i] == '-' || s[i] == '+'))
		{
			if (s[i] == '-' || s[i] == '+')
			{
				if (!(s[i + 1] >= '0' && s[i + 1] <= '9'))
					return (0);
			}
			return (1);
		}
		i++;
	}
	return (0);
}
